/**
 * Конфигурация Prettier для проекта L_Shop
 * 
 * Prettier - форматировщик кода с опinionated настройками.
 * Автоматически форматирует код при сохранении файла.
 * 
 * Документация: https://prettier.io/docs/en/options.html
 * 
 * Использование:
 * - npm run format - форматировать все файлы
 * - VS Code: Shift + Alt + F - форматировать текущий файл
 */
module.exports = {
  // ============================================
  // Кавычки
  // ============================================
  // Использовать одинарные кавычки вместо двойных
  // Причина: одинарные кавычки - стандарт в JavaScript сообществе
  singleQuote: true,

  // ============================================
  // Запятые
  // ============================================
  // Добавлять запятую после последнего элемента в массивах и объектах
  // Причина: упрощает добавление новых элементов (меньше изменений в git diff)
  trailingComma: 'all',

  // ============================================
  // Ширина строки
  // ============================================
  // Максимальная длина строки - 100 символов
  // Причина: баланс между читаемостью и компактностью
  printWidth: 100,

  // ============================================
  // Отступы
  // ============================================
  // Использовать 2 пробела для отступов
  // Причина: стандарт для JavaScript/TypeScript проектов
  tabWidth: 2,

  // Использовать пробелы вместо табуляции
  useTabs: false,

  // ============================================
  // Скобки
  // ============================================
  // Добавлять пробел внутри фигурных скобок объектов
  // Пример: { name: 'Ivan' } вместо {name: 'Ivan'}
  bracketSpacing: true,

  // ============================================
  // Точка с запятой
  // ============================================
  // Добавлять точку с запятой в конце выражений
  // Причина: предотвращает ошибки при минификации
  semi: true,

  // ============================================
  // JSX
  // ============================================
  // Использовать одинарные кавычки в JSX
  jsxSingleQuote: true,

  // Переносить закрывающую скобку JSX на новую строку
  bracketSameLine: false,

  // ============================================
  // Стрелочные функции
  // ============================================
  // Всегда использовать скобки вокруг параметров стрелочной функции
  // Пример: (x) => x вместо x => x
  arrowParens: 'always',

  // ============================================
  // Конец строки
  // ============================================
  // Использовать LF (Unix-style) для конца строк
  // Причина: стандарт для Git и Linux/macOS
  endOfLine: 'lf',
};
