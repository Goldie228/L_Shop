/**
 * Компонент Icon
 * Рендерит SVG иконку по имени
 */
export class Icon extends Component<IconProps> {
  /**
   * Получить пропсы по умолчанию
   */
  protected getDefaultProps(): IconProps {
    return {
      ...super.getDefaultProps(),
      size: 24,
      className: 'icon',
    };
  }

  /**
   * Отрендерить иконку
   * @returns Контейнер span с SVG внутри (тип HTMLElement)
   */
  public render(): HTMLElement {
    const {
      name, size, className, ariaLabel,
    } = this.props;

    // Создаем контейнер span
    const container = this.createElement('span', {
      className: `icon-container ${className}`,
    });

    // Создаем SVG элемент
    const svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg') as SVGSVGElement;

    // Устанавливаем атрибуты SVG (size гарантированно есть из getDefaultProps)
    svg.setAttribute('viewBox', '0 0 24 24');
    svg.setAttribute('width', (size ?? 24).toString());
    svg.setAttribute('height', (size ?? 24).toString());
    svg.setAttribute('fill', 'currentColor');
    svg.setAttribute('aria-hidden', 'true');

    if (ariaLabel) {
      svg.setAttribute('aria-label', ariaLabel);
      svg.setAttribute('role', 'img');
    }

    // Вставляем путь иконки
    const path = document.createElementNS('http://www.w3.org/2000/svg', 'path');
    path.setAttribute('d', ICON_PATHS[name]);
    svg.appendChild(path);

    // Добавляем SVG в контейнер
    container.appendChild(svg);

    this.element = container;
    return container;
  }

  /**
   * Получить SVG путь для иконки (статический метод для использования без создания компонента)
   */
  public static getPath(name: IconName): string {
    return ICON_PATHS[name];
  }
}
